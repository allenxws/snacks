CREATE TABLE `product_info` (
  `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键id',
  `product_id` bigint(20) NOT NULL DEFAULT '0' COMMENT '关联product.id',
  `brand` varchar(128) DEFAULT NULL COMMENT '品牌',
  `product_name` varchar(128) DEFAULT NULL COMMENT '产品名称',
  `quality_period` varchar(128) DEFAULT NULL COMMENT '保质期',
  `produce_date` varchar(128) DEFAULT NULL COMMENT '生产日期',
  `volume` varchar(128) DEFAULT NULL COMMENT '净含量',
  `package_way` varchar(128) DEFAULT NULL COMMENT '包装方式',
  `produce_place` varchar(128) DEFAULT NULL COMMENT '产地',
  `storage_way` varchar(128) DEFAULT NULL COMMENT '储藏方式',
  `technology` varchar(128) DEFAULT NULL COMMENT '食品工艺',
  `contain_sugar` varchar(128) DEFAULT NULL COMMENT '是否含糖',
  `ingredients` varchar(128) DEFAULT NULL COMMENT '配料',
  `produce_permit` varchar(128) DEFAULT NULL COMMENT '生产许可证编号',
  `bar_code` varchar(128) DEFAULT NULL COMMENT '商品条形码',
  `product_code` varchar(128) DEFAULT NULL COMMENT '产品标准号',
  `is_deleted` tinyint(1) NOT NULL DEFAULT '0' COMMENT '删除字段，0未删除1已删除',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='产品信息表'